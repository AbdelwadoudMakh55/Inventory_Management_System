    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
    try {
        Connection conn = DBConnection.establish_conn();
        if (conn == null) {
            System.out.println("Failed to establish a database connection.");
            return;
        }

        StringBuilder queryBuilder = new StringBuilder("SELECT SupplierID, SupplierName, ContactPerson, Phone, Email FROM supplier WHERE");
        ArrayList params = new ArrayList();

        int supplierID = parseIntOrDefault(id_field.getText().trim());
        if (supplierID != 0) {
            queryBuilder.append(" SupplierID = ?");
            params.add(supplierID);
        }

        String supplierName = name_field.getText().trim();
        if (!supplierName.isEmpty()) {
            if (params.size() > 0) {
                queryBuilder.append(" OR");
            }
            queryBuilder.append(" SupplierName LIKE ?");
            params.add("%" + supplierName + "%");
        }
        String ContactPerson = contact_field.getText().trim();
        if (!ContactPerson.isEmpty()) {
            if (params.size() > 0) {
                queryBuilder.append(" OR");
            }
            queryBuilder.append(" ContactPerson LIKE ?");
            params.add("%" + ContactPerson + "%");
        }
        String phone = phone_field.getText().trim();
        if (!phone.isEmpty()) {
            if (params.size() > 0) {
                queryBuilder.append(" OR");
            }
            queryBuilder.append(" Phone LIKE ?");
            params.add("%" + phone + "%");
        }
        String email = email_field.getText().trim();
        if (!supplierName.isEmpty()) {
            if (params.size() > 0) {
                queryBuilder.append(" OR");
            }
            queryBuilder.append(" email LIKE ?");
            params.add("%" + email + "%");
        }

        try (PreparedStatement ps = conn.prepareStatement(queryBuilder.toString())) {
            for (int i = 0; i < params.size(); i++) {
                ps.setObject(i + 1, params.get(i));
            }

            try (ResultSet rs = ps.executeQuery()) {
                DefaultTableModel dtm = (DefaultTableModel) suppliers_list.getModel();
                dtm.setRowCount(0);

                ResultSetMetaData rsmd = rs.getMetaData();
                int col_n = rsmd.getColumnCount();

                while (rs.next()) {
                    Vector<Object> row = new Vector<>();
                    for (int i = 1; i <= col_n; i++) {
                        row.add(rs.getObject(i));
                    }
                    dtm.addRow(row);
                }
            }
        }
    } catch (SQLException e) {
        System.out.println("SQL Error: " + e.getMessage());
    }
    }//GEN-LAST:event_jButton1ActionPerformed

