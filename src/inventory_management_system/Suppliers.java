    private void addsupplier(Connection conn, String suppName, String contact, int ID, String phone, String email) {
        try {
            String insertQuery = "INSERT INTO supplier (SupplierID, SupplierName, ContactPerson, Phone, Email) VALUES (?, ?, ?, ?, ?)";

            try (PreparedStatement ps = conn.prepareStatement(insertQuery, Statement.RETURN_GENERATED_KEYS)) {
                ps.setInt(1, ID);
                ps.setString(2, suppName);
                ps.setString(3, contact);
                ps.setString(4, phone);
                ps.setString(5, email);
                int affectedRows = ps.executeUpdate();
            }
        } catch (SQLException e) {
            System.out.println("SQL Error: " + e.getMessage());
        }
    }
    private int parseIntOrDefault(String input) {
        try {
            return Integer.parseInt(input);
        } catch (NumberFormatException e) {
            return 0;
        }
}
