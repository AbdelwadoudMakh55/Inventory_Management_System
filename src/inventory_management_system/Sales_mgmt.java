    private void search_buttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_search_buttonActionPerformed
    try {
        Connection conn = DBConnection.establish_conn();
        StringBuilder queryBuilder = new StringBuilder("SELECT ProductID, ProductName, CategoryID, SupplierID, Price, QuantityInStock, ReorderLevel, ReorderQuantity, DateAdded FROM Product WHERE");
        ArrayList params = new ArrayList();

        int productId = parseIntOrDefault(ID_field.getText().trim());
        if (productId != 0) {
            queryBuilder.append(" ProductID = ?");
            params.add(productId);
        }

        String productName = Name_field.getText().trim();
        if (!productName.isEmpty()) {
            if (!params.isEmpty()) {
                queryBuilder.append(" OR");
            }
            queryBuilder.append(" ProductName LIKE ?");
            params.add("%" + productName + "%");
        }

        int categoryId = parseIntOrDefault(categoryID_field.getText().trim());
        if (categoryId != 0) {
            if (!params.isEmpty()) {
                queryBuilder.append(" OR");
            }
            queryBuilder.append(" CategoryID = ?");
            params.add(categoryId);
        }

        try (PreparedStatement ps = conn.prepareStatement(queryBuilder.toString())) {
            for (int i = 0; i < params.size(); i++) {
                ps.setObject(i + 1, params.get(i));
            }

            try (ResultSet rs = ps.executeQuery()) {
                DefaultTableModel dtm = (DefaultTableModel) product_list.getModel();
                dtm.setRowCount(0);

                ResultSetMetaData rsmd = rs.getMetaData();
                int col_n = rsmd.getColumnCount();

                while (rs.next()) {
                    Vector<Object> row = new Vector<>();
                    row.add(false);
                    for (int i = 1; i <= col_n; i++) {
                        row.add(rs.getObject(i));
                    }
                    dtm.addRow(row);
                }
            }
        }
    } catch (SQLException e) {
        System.out.println("SQL Error: " + e.getMessage());
    }
    }//GEN-LAST:event_search_buttonActionPerformed

    private void add_cart_buttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_add_cart_buttonActionPerformed
        DefaultTableModel dtm = (DefaultTableModel) product_list.getModel();
        DefaultTableModel ctm = (DefaultTableModel) cart_list.getModel();
        int rows;
        int n_rows = dtm.getRowCount();
        for (rows = 0; rows < n_rows; rows++) {
            boolean boolValue = ((Boolean) dtm.getValueAt(rows, 0));
            if (boolValue != false) {
                int columnIndex = product_list.getColumn("Price").getModelIndex();
                if (Float.parseFloat(dtm.getValueAt(rows, columnIndex).toString()) > 0) {
                    Vector<Object> row = new Vector<>();
                    row.add(dtm.getValueAt(rows, 1));
                    row.add(dtm.getValueAt(rows, 2));
                    row.add(dtm.getValueAt(rows, 6));
                    float total_price = Float.parseFloat(dtm.getValueAt(rows, 6).toString()) * Float.parseFloat(dtm.getValueAt(rows, 5).toString());
                    row.add(total_price);
                    ctm.addRow(row);
                }
            }
        }
    }//GEN-LAST:event_add_cart_buttonActionPerformed

